    public EmergencyServicesJPanel(JPanel userProcessContainer, UserAccount account, Business business) {
        initComponents();
        txtInstructions.setVisible(false);
        this.networkString = account.getNetwork();

        if (business.getNetworkList() == null) {
            this.network = new HashMap<String, Enterprise>();
        } else {
            this.network = business.getNetworkList();
        }

        this.enterprise = business.findEnterpriseByNetwork(account.getNetwork());

        this.managerName = account.getName();
        this.flags = new FlagClass();

        if (enterprise.getEmergencyServices() == null) {
            this.emergencyServiceDirectory = new EmergencyServicesDirectory();
        } else {
            this.emergencyServiceDirectory = enterprise.getEmergencyServices();
        }

        emergencyServiceFetch = emergencyServiceDirectory.findEmergencyServiceByManagerName(managerName);

        if (emergencyServiceFetch.getStaffDirectory() == null) {
            this.staffDirectory = new StaffDirectory();
        } else {
            this.staffDirectory = emergencyServiceFetch.getStaffDirectory();
        }

        if (emergencyServiceFetch != null) {
            lblAmbulanceCount.setText(String.valueOf(emergencyServiceFetch.getNumOfAmbulances()));
            lblFireBrigadeCount.setText(String.valueOf(emergencyServiceFetch.getNumOfFireBrigades()));
            lblFirstAidKitCount.setText(String.valueOf(emergencyServiceFetch.getNumOfFirstAidKits()));
            lblAirAmbulanceCount.setText(String.valueOf(emergencyServiceFetch.getNumOfAirAmbulance()));
        } else {
            lblAmbulanceCount.setText("--");
            lblFireBrigadeCount.setText("--");
            lblFirstAidKitCount.setText("--");
            lblAirAmbulanceCount.setText("--");
        }

        populateStaff();

    }

